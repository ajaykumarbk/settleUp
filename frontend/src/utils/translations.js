// Helper utility to translate a key
export function t(key) {
  let lang = 'en';
  try {
    lang = localStorage.getItem('splitwise_language') || 'en';
  } catch {}
  
  if (!translations[lang]) lang = 'en';
  return translations[lang][key] || translations['en'][key] || key;
}
